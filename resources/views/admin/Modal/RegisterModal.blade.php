                    $("form").on('submit',function(e){
                        e.preventDefault();
                        var formData=$('form').serializeArray();
        
                        $.ajax({
                            url:"{{url('StoreModal')}}",
                            type:'post',
                            data:formData,
                            dataType:'json',
                            beforeSend:function(){
                                $(".save-data").addClass('disabled').text('Loading...');
                            },
                            success:function(res){
                                $(".ajax-res").text('Data has been added');
                                $(".save-data").removeClass('disabled').text('Submit');
                            }
                        });
                    });
                });
